#include "LlamaXML/LlamaStdIO.h"
#include <stdarg.h>
#include <stdio.h>

namespace LlamaXML {
	int snprintf(char *buffer, size_t count, const char *format, ...) {
		va_list args;
		va_start(args, format);
		int result = vsnprintf(buffer, count - 1, format, args);
		buffer[count - 1] = 0;
		return result;
	}
}
